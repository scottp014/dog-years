// my age
const myAge = 31;

// first two years of a dog's life equivalent to human years
let earlyYears = 2;

earlyYears *= 10.5;

// years equal to early dog's age subtracted
let laterYears = myAge - 2;

// age of dog after first two years of life
laterYears *= 4;

// earlyYears and laterYears combined to make total age in dog years
myAgeInDogYears = earlyYears + laterYears;

// returns my name with all lowercase letters
var myName = 'Scott'.toLowerCase();

// string that states my name in dog years
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);
